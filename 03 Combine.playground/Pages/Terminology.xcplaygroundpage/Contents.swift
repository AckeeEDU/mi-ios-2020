//: # Terminology
//:
//: ## Producer
//:
//: You got `Publisher`s that emit values thus typically publish a stream of events
//:
//: ## Consumer
//:
//: To receive events, you need to subscribe to a certain stream of events so the consumer is called `Subscriber`
//:
//: ## Operators
//:
//: Sometimes (well very often, okay almost everytime) when you use a `Publisher` you need to combine more streams, change their output, etc. so you are given a bunch of operators that will cover most of your cases
